package com.example.chapter06;

public class exam1 {
    public static void main(String[]args){
        int a = 6, b = 4;
        System.out.println(a + " + " + b + " = " + (a + b));
        System.out.println(a + " - " + b + " = " + (a - b));
        System.out.println(a + " * " + b + " = " + (a * b));
        System.out.println(a + " / " + b + " = " + (a / b));
        System.out.println(a + " / " + b + " = " + ((float)a / b));
    }
}
